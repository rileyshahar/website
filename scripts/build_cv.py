# ---------------------------------------------------------------- utilities

_TYPST_ESCAPES = {
    "\\": "\\\\",
    "#": "\\#",
    "$": "\\$",
    "@": "\\@",
    "*": "\\*",
    "_": "\\_",
    "`": "\\`",
    "<": "\\<",
    ">": "\\>",
    "[": "\\[",
    "]": "\\]",
}


def ty(s: Any) -> str:
    """Escape a string for use inside a Typst content block ``[...]``."""
    if s is None:
        return ""
    return "".join(_TYPST_ESCAPES.get(ch, ch) for ch in str(s))


def code_str(s: Any) -> str:
    """Escape a string for use inside a Typst double-quoted code-mode string."""
    if s is None:
        return ""
    return str(s).replace("\\", "\\\\").replace('"', '\\"')


_MD_INLINE_RE = re.compile(
    r"(\[[^\]]+\]\([^)]+\)|\$\$[^$]+\$\$|\$[^$\n]+\$|`[^`\n]+`|\*\*[^*\n]+\*\*|__[^_\n]+__|(?<!\*)\*[^*\n]+\*(?!\*)|(?<!_)_[^_\n]+_(?!_))"
)
_MD_LINK_RE = re.compile(r"^\[([^\]]+)\]\(([^)]+)\)$")


def md(s: Any) -> str:
    """Convert inline Markdown from YAML data into Typst content markup."""
    if s is None:
        return ""
    out = []
    for part in re.split(_MD_INLINE_RE, str(s)):
        if not part:
            continue
        if match := _MD_LINK_RE.match(part):
            label, url = match.groups()
            out.append(f'#link("{code_str(url)}")[{md(label)}]')
        elif (part.startswith("$$") and part.endswith("$$")) or (
            part.startswith("$") and part.endswith("$")
        ):
            out.append(f"${part.strip('$')}$")
        elif part.startswith("`") and part.endswith("`"):
            out.append(part)
        elif (part.startswith("**") and part.endswith("**")) or (
            part.startswith("__") and part.endswith("__")
        ):
            out.append(f"*{md(part[2:-2])}*")
        elif (part.startswith("*") and part.endswith("*")) or (
            part.startswith("_") and part.endswith("_")
        ):
            out.append(f"_{md(part[1:-1])}_")
        else:
            out.append(ty(part))
    return "".join(out)


def initialize(name: str) -> str:
    """'Kyle Ormsby' -> 'K. Ormsby'. Leaves already-initialized names alone."""
    name = name.strip()
    if re.match(r"^[A-Z]\.\s", name):
        return name
    parts = name.split()
    if len(parts) < 2:
        return name
    return f"{parts[0][0]}. {' '.join(parts[1:])}"


def coauthor_name(c: Any) -> str:
    if isinstance(c, dict):
        return str(c.get("name", ""))
    return str(c)


def date_key(d: Any):
    if isinstance(d, datetime.date):
        return d.isoformat()
    return str(d) if d else ""


def year_of(d: Any) -> str:
    if isinstance(d, datetime.date):
        return str(d.year)
    return str(d)[:4] if d else ""


_MONTHS = [
    "",
    "January",
    "February",
    "March",
    "April",
    "May",
    "June",
    "July",
    "August",
    "September",
    "October",
    "November",
    "December",
]


def month_year(d: Any) -> str:
    if isinstance(d, datetime.date):
        return f"{_MONTHS[d.month]} {d.year}"
    try:
        parsed = datetime.date.fromisoformat(str(d))
    except (TypeError, ValueError):
        return str(d) if d else ""
    return f"{_MONTHS[parsed.month]} {parsed.year}"


def long_date(d: datetime.date) -> str:
    return f"{_MONTHS[d.month]} {d.day}, {d.year}"


def load(name: str):
    path = DATA / f"{name}.yaml"
    if not path.exists():
        return []
    return yaml.safe_load(path.read_text()) or []


# ----------------------------------------------------------------- template

PREAMBLE = """// Auto-generated by scripts/build_cv.py. Do not edit by hand.
#import "@preview/pro-academic-cv:0.1.0": *

#let cv-date(body) = box[#text(size: 0.9em, style: "normal")[#body]]

#let cv-row2col(left, rhs) = grid(
  columns: (1fr, auto),
  column-gutter: 0.5em,
  [#left],
  [#cv-date(rhs)],
)

"""

DATE_TEXT_ARGS = '(size: 0.9em, style: "normal")'


# --------------------------------------------------------- rendering helpers


def _r2c2_entry(
    *,
    top_left: str = "",
    top_right: str = "",
    bottom_left: str = "",
    bottom_right: str = "",
    top_right_args: str | None = None,
    list_items: list[str] | None = None,
) -> str:
    """Emit a single entry dict for ``r2c2-entry-list``."""
    lines = [
        "  (",
        "    entry-header-args: (",
        f"      top-left: [{top_left}],",
        f"      top-right: [{top_right}],",
        f"      bottom-left: [{bottom_left}],",
        f"      bottom-right: [{bottom_right}],",
    ]
    if top_right_args:
        lines.append(f"      top-right-args: {top_right_args},")
    lines.append("    ),")
    if list_items:
        lines.append("    list-items: (")
        for item in list_items:
            lines.append(f"      [{item}],")
        lines.append("    ),")
    lines.append("  )")
    return "\n".join(lines)


def _r2c2_list(title: str, entries: list[str]) -> str:
    body = ",\n".join(entries)
    return f"== {title}\n\n#r2c2-entry-list(\n{body},\n)\n\n"


def _row_list(title: str, rows: list[tuple[str, str]]) -> str:
    """Emit ``multi-line-list(row2col(...), ...)`` for a flat bulleted list."""
    lines = [f"  cv-row2col([{left}], [{right}])" for left, right in rows]
    body = ",\n".join(lines)
    return f"== {title}\n\n#multi-line-list(\n{body},\n)\n\n"


def _join_coauthors(coauthors: list) -> str:
    names = [initialize(coauthor_name(c)) for c in coauthors]
    names = [md(n) for n in names if n]
    if not names:
        return ""
    if len(names) == 1:
        return f"Joint with {names[0]}."
    if len(names) == 2:
        return f"Joint with {names[0]} and {names[1]}."
    return f"Joint with {', '.join(names[:-1])}, and {names[-1]}."


# ----------------------------------------------------------------- sections


def header(cv: dict) -> str:
    name = f"{cv.get('first', '')} {cv.get('last', '')}".strip()

    primary: list[str] = []
    if cv.get("email"):
        email = cv["email"]
        primary.append(f'#link("mailto:{code_str(email)}")[{ty(email)}]')
    if cv.get("website"):
        display = md(cv.get("website_display") or cv["website"])
        primary.append(f'#link("{code_str(cv["website"])}")[{display}]')

    secondary: list[str] = []
    if cv.get("pronouns"):
        secondary.append(f"Pronouns: {md(cv['pronouns'])}")
    if cv.get("office"):
        secondary.append(f"Office: {md(cv['office'])}")

    tertiary_parts: list[str] = []
    if cv.get("department"):
        tertiary_parts.append(md(cv["department"]))
    if cv.get("affiliation"):
        tertiary_parts.append(md(cv["affiliation"]))
    tertiary = ", ".join(tertiary_parts)

    return (
        "#show: resume.with(\n"
        "  author-info: (\n"
        f'    name: "{code_str(name)}",\n'
        f"    primary-info: [{' | '.join(primary)}],\n"
        f"    secondary-info: [{' | '.join(secondary)}],\n"
        f"    tertiary-info: [{tertiary}],\n"
        "  ),\n"
        ")\n\n"
    )


def interests_section(cv: dict) -> str:
    ri = cv.get("research_interests")
    if not ri:
        return ""
    return f"== Research interests\n\n{md(str(ri).strip())}\n\n"


